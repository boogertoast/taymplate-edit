  function flatten(obj, prefix) {
    const out = {};
    for (const [k, v] of Object.entries(obj)) {
      const key = prefix ? `${prefix}.${k}` : k;
      if (v !== null && typeof v === 'object' && !Array.isArray(v)) {
        Object.assign(out, flatten(v, key));
      } else {
        out[key] = String(v);
      }
    }
    return out;
  }
  const tokens = flatten(SITE, '');
  function resolve(val) {
    return val.replace(/\{\{([^}]+)\}\}/g, (_, key) => tokens[key] ?? `{{${key}}}`);
  }

  function walkText(node) {
    if (node.nodeType === Node.TEXT_NODE) {
      if (node.nodeValue.includes('{{')) node.nodeValue = resolve(node.nodeValue);
    } else {
      node.childNodes.forEach(walkText);
    }
  }
  walkText(document.body);

  document.querySelectorAll('img[src]').forEach(el => {
    el.src = resolve(el.getAttribute('src'));
  });
  document.querySelectorAll('source[srcset]').forEach(el => {
    el.srcset = resolve(el.getAttribute('srcset'));
  });
  document.querySelectorAll('[style]').forEach(el => {
    const s = el.getAttribute('style');
    if (s.includes('{{')) el.setAttribute('style', resolve(s));
  });

  document.querySelectorAll('link[href], meta[content]').forEach(el => {
    const attr = el.tagName === 'LINK' ? 'href' : 'content';
    const val = el.getAttribute(attr) || '';
    if (val.includes('{{')) el.setAttribute(attr, resolve(val));
  });

